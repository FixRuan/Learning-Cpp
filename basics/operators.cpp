#include <iostream>

using namespace std;

int main() {
  int a = 10, b = 5;

  cout << "Variaveis: "
       << "a: " << a << " b: " << b << endl;

  cout << "Soma: " << a + b << endl;
  cout << "Subtracao: " << a - b << endl;
  cout << "Multiplicacao: " << a * b << endl;
  cout << "Divisao: " << a / b << endl;

  return 0;
}
