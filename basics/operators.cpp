#include <iostream>
#include <math.h>

using namespace std;

int main() {
  int a = 5, b = 2;

  cout << "Variaveis: "
       << "a: " << a << " b: " << b << endl;

  cout << "Soma: " << a + b << endl;
  cout << "Subtracao: " << a - b << endl;
  cout << "Multiplicacao: " << a * b << endl;
  cout << "Divisao: " << a / b << endl;
  cout << "Potencia: " << pow(a, b) << endl;

  return 0;
}
